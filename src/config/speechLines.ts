export function getRandomLine(
  category: string,
  userName?: string,
  overrides?: Partial<Record<string, string[]>>
): string {
  let lines: string[] | undefined;

  if (overrides && overrides[category] && overrides[category]!.length > 0) {
    if (Math.random() < 0.4) {
      lines = overrides[category];
    }
  }

  if (!lines) {
    lines = SPEECH_LINES[category];
  }
  if (!lines || lines.length === 0) return '';
  const line = lines[Math.floor(Math.random() * lines.length)];
  return line.replace('{name}', userName || 'friend');
}
